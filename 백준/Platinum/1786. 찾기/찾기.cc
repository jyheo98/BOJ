using pii = pair<int, int>;
using ll = long long;
const ll MOD = 1e9 + 7;
const long double PI = acos(-1.0);

string p, s;
vector<int> fail, ans;

void find_fail() {
	int n = sz(p), j = 0;
	for(int i=1 ; i<n ; i++) {
		while(j>0 && p[i]!=p[j])
			j = fail[j-1];
		if(p[i]==p[j])
			fail[i] = ++j;
	}
}

void kmp() {
	int n = sz(s), m = sz(p), j=0;
	for(int i=0 ; i<n ; i++) {
		while(j>0 && s[i]!=p[j])
			j = fail[j-1];
		if(s[i]==p[j]) {
			if(j==m-1) {
				ans.push_back(i-m+1);
				j = fail[j];
			} else
				j++;
		}
	}
}

void solve() { 
	getline(cin, s);
	getline(cin, p);
	fail = vector<int>(sz(p));
	find_fail();
	kmp();
	cout << sz(ans) << "\n";
	for(auto x : ans) {
		cout << x+1 << " ";
	}
	cout << "\n";
}

